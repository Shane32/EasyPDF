        /// Left-top alignment.
        /// </summary>
        LeftTop,
        /// <summary>
        /// Left-center alignment.
        /// </summary>
        LeftCenter,
        /// <summary>
        /// Left-bottom alignment.
        /// </summary>
        LeftBottom,
        /// <summary>
        /// Center-top alignment.
        /// </summary>
        CenterTop,
        /// <summary>
        /// Center-center alignment.
        /// </summary>
        CenterCenter,
        /// <summary>
        /// Center-bottom alignment.
        /// </summary>
        CenterBottom,
        /// <summary>
        /// Right-top alignment.
        /// </summary>
        RightTop,
        /// <summary>
        /// Right-center alignment.
        /// </summary>
        RightCenter,
        /// <summary>
        /// Right-bottom alignment.
        /// </summary>
        RightBottom,
    }
}
